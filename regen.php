						<dt><h3>Mission Statement</h3></dt>
						<dd><p>I believe in a higher power, the holy trinity: the father, sin, and 
						holy spirit. I believe God to be loving to a degree that I can't fully 
						ever understand. I believe that that I am flawed, not just what I do 
						but how I think and feel. I believe that these are fatal flaws, as they 
						should be. Even the smallest lie. It is through Christ alone that I am saved.
						</br></br>		
						I know there is nothing in my power to make me new or cleanse me of 
						these faults, and I am called to face them and any other new sins that 
						will ultimately come up in the future with the strength, grace and 
						guidance of God and His people. In proclaiming this I proclaim I am a 
						child of God, and will actively seek to do right by God and His word. 
						</br></br>
						I want to be humbled in my transgressions so that I can love more like 
						Jesus Christ does. I want to show of God's love and glory rather than 
						tell of it. I want to listen to the sufferings of others and sit with 
						them in that pain. I want to see God's influence in myself and others 
						around me. I esnt to be a part of God's plan and goodness. I want to 
						love others in a way that doesn't seek validation and praise, but in a 
						way that genuinely helps the other person. Never have I seen judgements 
						and threats from people do such a thing as what I hope for. So while I 
						will speak up if something is wrong or needs to be discussed for 
						clarification. I will not condemn others or judge them. 
						</br></br>
						They are no worse than me, just humans who are plagued by sin. While I 
						struggle to give grace to everyone, they are no less deserving of it 
						than anyone else according to God. I am no rescuer. It is not me who 
						saves but Jesus Christ, for I will always be in need of rescuing. It is 
						not my job to fix people but give people ways to help themselves instead,
